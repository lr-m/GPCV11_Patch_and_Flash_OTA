/* Blit a 24×24 sprite clipped to [clip_y0, clip_y1) */
static void draw_sprite_clipped(uint16_t *fb, int32_t x, int32_t y,
                                uint32_t sym,
                                int32_t clip_y0, int32_t clip_y1)
{
    const uint16_t *spr = sprites[mod7(sym)];
    for (int32_t row = 0; row < 24; row++) {
        int32_t sy = y + row;
        if (sy < clip_y0 || sy >= clip_y1) continue;
        if ((uint32_t)sy >= HEIGHT) continue;
        for (int32_t col = 0; col < 24; col++) {
            int32_t sx = x + col;
            if ((uint32_t)sx >= WIDTH) continue;
            fb[(uint32_t)sy * WIDTH + (uint32_t)sx] = spr[row * 24 + col];
        }
    }
}

/*
 * Draw one reel column as a scrolling strip of 24x24 sprites.
 *   pos    = symbol index in the BOTTOM slot (= result) when scroll == 0
 *   scroll = 0..SYM_H-1 pixels of downward motion within current step
 *
 * k=1 → top half,  k=2 → bottom half (result).  sym(k) = (pos+9-k) % 7.
 */
static void draw_reel_anim(uint16_t *fb, uint32_t col,
                           uint32_t pos, uint32_t scroll)
{
    int32_t rx = (int32_t)reel_x(col);
    /* k=-1: sprite above screen that scrolls in from top as scroll increases */
    for (int32_t k = -1; k < 4; k++) {
        int32_t y = (int32_t)REEL_Y + k * (int32_t)SYM_H
                    + (int32_t)scroll - (int32_t)SYM_H;
        /* k=1 → result. Cast k to uint32_t: k=-1 wraps to give offset +9 */
        uint32_t sym = mod7(pos + 8u - (uint32_t)k);
        draw_sprite_clipped(fb, rx, y, sym, 0, HEIGHT);
    }
}



/* Draw slot-machine chrome: payline bars and side pointer triangles */
static void draw_chrome(uint16_t *fb)
{
    /* Payline white lines framing the result row */
    draw_rect(fb, 0, REEL_Y - 1,         140, 1, WHITE);
    draw_rect(fb, 0, REEL_Y + SYM_H - 1, 140, 1, WHITE);
    /* Left pointer triangle (▶), tip at right */
    draw_rect(fb, 0, 37, 1, 7, RED);
    draw_rect(fb, 1, 38, 1, 5, RED);
    draw_rect(fb, 2, 39, 1, 3, RED);
    draw_rect(fb, 3, 40, 1, 1, RED);
}

/*
 * Animated slot-machine handle on the left side.
 * anim=0: idle (knob at top).  anim 1-8: pull down.  anim 9-16: spring back.
 */
static void draw_handle(uint16_t *fb, uint32_t anim)
{
    /*
     * Pivot fixed at y=40 (mid-screen). Arm length 28px.
     * LUT precomputes knob Y via sin curve: slow at top/bottom, fast through middle.
     * Frames 0=idle (knob at top, y=12), 1-8=pull down through pivot, 8=full pull (y=68).
     * Frames 9-16 mirror back to idle.
     */
    static const uint8_t __attribute__((section(".text"))) ky_lut[17] = {
        12, 14, 20, 29, 40, 51, 60, 66, 68, 66, 60, 51, 40, 29, 20, 14, 12
    };
    uint32_t ky   = (uint32_t)ky_lut[anim <= 16u ? anim : 0u];
    uint32_t xarm = (uint32_t)(WIDTH - 11u);
    uint32_t ypiv = 40u;

    /* Pivot marker — small grey square where arm attaches to machine body */
    draw_rect(fb, (int32_t)xarm - 1, (int32_t)ypiv - 2, 4, 4, 0x8410u);

    /* Arm (grey rod) — foreshortens to nothing as knob passes through pivot */
    if (ky + 10u < ypiv) {
        /* Knob above pivot: arm from knob-bottom down to pivot */
        uint32_t top = ky + 10u;
        if (ypiv > top)
            draw_rect(fb, (int32_t)xarm, (int32_t)top, 2, (int32_t)(ypiv - top), 0x8410u);
    } else if (ky > ypiv + 4u) {
        /* Knob below pivot: arm from pivot down to knob-top */
        draw_rect(fb, (int32_t)xarm, (int32_t)(ypiv + 4u), 2,
                  (int32_t)(ky - ypiv - 4u), 0x8410u);
    }
    /* When knob is near pivot: arm invisible — handle appears to swing through */

    /* Knob body */
    draw_rect(fb, (int32_t)(WIDTH - 18u), (int32_t)ky,      15, 11, 0x8410u);
    draw_rect(fb, (int32_t)(WIDTH - 17u), (int32_t)ky,      14, 10, 0xA000u);
    draw_rect(fb, (int32_t)(WIDTH - 14u), (int32_t)(ky+1u), 10,  6, RED);
    draw_rect(fb, (int32_t)(WIDTH - 11u), (int32_t)(ky+2u),  6,  3, WHITE);
}

/* Noinline stub — must stay immediately before slot_machine_task so adr fits. */
static __attribute__((noinline)) uint32_t get_slot_task_addr(void) {
    uint32_t p;
    asm volatile("adr %0, slot_machine_task" : "=r"(p));
    return p;
}

void slot_machine_task(void *arg)
{
    dbg_print_fn          dbg_print  = (dbg_print_fn)DBG_PRINT_ADDR;
    frame_buff_display_fn fb_display = (frame_buff_display_fn)AP_SETTING_FRAME_BUFF_DISPLAY;
    sleep_fn              msleep     = (sleep_fn)SLEEP_ADDR;
    play_sound_fn         play_sound = (play_sound_fn)PLAY_SOUND;

    volatile uint32_t *active   = state_active();
    volatile uint32_t *spin_req = state_spin_req();

    dbg_print("slot_machine_task: started\n");

    uint32_t reel_pos[NUM_REELS];
    uint32_t reel_scroll[NUM_REELS];
    uint32_t reel_stopped[NUM_REELS];
    uint32_t reel_wants_stop[NUM_REELS];
    uint32_t target[NUM_REELS];
    uint32_t state, spin_frame, spin_speed, decel_tick, result_tick, handle_anim;
    /* prng and spin_tick persist across restart so they keep accumulating entropy */
    uint32_t prng = 0xDEADBEEFu;
    uint32_t spin_tick = 0;

restart:
    reel_pos[0] = reel_pos[1] = reel_pos[2] = 0;
    reel_scroll[0] = reel_scroll[1] = reel_scroll[2] = 0;
    reel_stopped[0] = reel_stopped[1] = reel_stopped[2] = 0;
    reel_wants_stop[0] = reel_wants_stop[1] = reel_wants_stop[2] = 0;
    state = STATE_IDLE;
    spin_frame = 0;
    spin_speed = 8;
    result_tick = 0;
    handle_anim = 0;

    while (*active == 1) {
        uint16_t *fb = get_fb();
        if (!fb) { msleep(50); continue; }
        /* Advance handle animation */
        if (handle_anim > 0u) {
            if (++handle_anim > 16u) handle_anim = 0;
        }

        if (state == STATE_IDLE) {
            spin_tick++;
            /* Keep PRNG churning every idle frame — output depends on exact
             * button-press timing, giving genuine entropy                    */
            prng ^= prng << 13;
            prng ^= prng >> 17;
            prng ^= prng << 5;
            prng ^= spin_tick;   /* mix in frame count for extra diffusion */

            fill_fb(BLACK);
            for (uint32_t i = 0; i < NUM_REELS; i++)
                draw_reel_anim(fb, i, reel_pos[i], 0);
            draw_chrome(fb);
            draw_handle(fb, handle_anim);
            /* "SPIN!" on gold background, centred */
            draw_rect(fb, 57, 6, 46, 14, GOLD);
            draw_text(fb, "SPIN!", 60, 9, WHITE);
            fb_display();

            if (*spin_req) {
                *spin_req = 0;
                /* PRNG is already well-mixed; just take 3 consecutive outputs */
                for (uint32_t i = 0; i < NUM_REELS; i++) {
                    prng ^= prng << 13;
                    prng ^= prng >> 17;
                    prng ^= prng << 5;
                    target[i] = mod7(prng & 0x7Fu);
                }
                /* Force-win: override all reels to the same symbol */
                if (*state_force_win()) {
                    *state_force_win() = 0;
                    target[1] = target[0];
                    target[2] = target[0];
                }
                reel_stopped[0] = reel_stopped[1] = reel_stopped[2] = 0;
                reel_wants_stop[0] = reel_wants_stop[1] = reel_wants_stop[2] = 0;
                spin_speed = 8;
                decel_tick = 0;
                spin_frame = 0;
                handle_anim = 1;
                state = STATE_SPINNING;
            }

        } else if (state == STATE_SPINNING) {
            spin_frame++;

            /* Global deceleration — halve every 12 frames (power-of-2 speeds ≤8 divide 24 cleanly) */
            if (++decel_tick >= 12u && spin_speed > 2u) {
                decel_tick = 0;
                spin_speed >>= 1;  /* 16→8→4→2 */
            }

            /* Trigger per-reel coast-to-stop: switch to speed=1 so each wrap
             * lands cleanly on scroll=0, then stop when pos hits target.      */
            if (spin_frame == 30) reel_wants_stop[0] = 1;
            if (spin_frame == 50) reel_wants_stop[1] = 1;
            if (spin_frame == 70) reel_wants_stop[2] = 1;

            /* Advance spinning reels */
            for (uint32_t i = 0; i < NUM_REELS; i++) {
                if (reel_stopped[i]) continue;
                /* Coasting reels crawl at 1 px/frame — every wrap is clean (scroll→0) */
                uint32_t spd = reel_wants_stop[i] ? 2u : spin_speed;
                reel_scroll[i] += spd;
                while (reel_scroll[i] >= SYM_H) {
                    reel_scroll[i] -= SYM_H;
                    if (++reel_pos[i] >= 7u) reel_pos[i] = 0;
                    play_sound(0);
                    /* Stop the moment pos clicks onto the target (scroll is 0) */
                    if (reel_wants_stop[i] && reel_pos[i] == target[i])
                        reel_stopped[i] = 1;
                }
            }

            fill_fb(BLACK);
            for (uint32_t i = 0; i < NUM_REELS; i++)
                draw_reel_anim(fb, i, reel_pos[i],
                               reel_stopped[i] ? 0 : reel_scroll[i]);
            draw_chrome(fb);
            draw_handle(fb, handle_anim);
            fb_display();

            if (reel_stopped[0] && reel_stopped[1] && reel_stopped[2]) {
                result_tick = 0;
                state = STATE_RESULT;
            }

        } else { /* STATE_RESULT */
            uint32_t jackpot = (target[0] == target[1] && target[0] == target[2]);
            result_tick++;

            fill_fb(BLACK);
            for (uint32_t i = 0; i < NUM_REELS; i++)
                draw_reel_anim(fb, i, reel_pos[i], 0);
            draw_chrome(fb);
            draw_handle(fb, handle_anim);

            if (jackpot) {
                if (result_tick == 1) play_sound(3);
                /* Flash JACKPOT! on gold bg every ~200ms */
                if ((result_tick >> 2) & 1u) {
                    draw_rect(fb, 45, 6, 70, 14, GOLD);
                    draw_text(fb, "JACKPOT!", 48, 9, WHITE);
                }
            } else {
                /* TRY AGAIN on red background */
                draw_rect(fb, 41, 6, 78, 14, RED_BG);
                draw_text(fb, "TRY AGAIN", 44, 9, WHITE);
            }
            fb_display();

            /* Hold result ~2 s (40 frames × 50 ms) then return to idle */
            if (result_tick >= 40u)
                state = STATE_IDLE;
        }

        msleep(50);  /* ~20 fps */
    }

    dbg_print("slot_machine_task: parked\n");
    while (*active != 1)
        msleep(1000);
    dbg_print("slot_machine_task: resumed\n");
    goto restart;
}

/* ── key_cave (mode button) — triggers a spin ────────────────────────────────*/

void key_cave(void *arg)
{
    *state_spin_req() = 1;
}

/* ── pw_key_cave (power button) — exit slot mode ────────────────────────────*/

void pw_key_cave(void *arg)
{
    dbg_print_fn dbg_print = (dbg_print_fn)DBG_PRINT_ADDR;

    volatile uint32_t *active = state_active();
    *active = 2;  /* park */

    volatile uint32_t *saved = state_handlers();
    *KEY_PW   = saved[0];
    *KEY_MODE = saved[1];
    *KEY_MENU = saved[2];

    dbg_print("pw_key_cave: restored, calling original\n");
    ((void (*)(void *))saved[2])(arg);
}

/* ── menu_key_cave (menu button) — force-win spin ───────────────────────────*/

void menu_key_cave(void *arg)
{
    *state_force_win() = 1;
    *state_spin_req()  = 1;
}

/* ── version_cave — entry point, spawns/wakes slot task ─────────────────────*/

void version_cave(void)
{
    dbg_print_fn dbg_print = (dbg_print_fn)DBG_PRINT_ADDR;

    uint32_t kc_addr, pc_addr, mc_addr;
    asm("adr %0, key_cave"      : "=r"(kc_addr));
    asm("adr %0, pw_key_cave"   : "=r"(pc_addr));
    asm("adr %0, menu_key_cave" : "=r"(mc_addr));

    volatile uint32_t *saved = state_handlers();
    saved[0] = *KEY_PW;
    saved[1] = *KEY_MODE;
    saved[2] = *KEY_MENU;

    *KEY_PW   = pc_addr;
    *KEY_MODE = kc_addr;
    *KEY_MENU = mc_addr;

    volatile uint32_t *active = state_active();

    if (*active == 0) {
        uint32_t task_fn_addr = get_slot_task_addr();
        *active = 1;
        ((os_task_create_fn)OS_TASK_CREATE)(
            (void *)task_fn_addr,
            "slots",
            0x200,  /* stack: 0x200 << 2 = 0x800 words = 8KB */
            0,
            2,
            0
        );
        dbg_print("version_cave: slot task spawned\n");
    } else {
        *active = 1;
        dbg_print("version_cave: slot task resumed\n");
    }
}
