/* ── State block in .text — adr-accessible from anywhere in the cave ─────────
 * [0] _task_active:    0=never spawned  1=running  2=parked
 * [1] _spin_requested: set by key_cave to trigger a spin
 *
 * IMPORTANT: the noinline getter functions below MUST remain immediately after
 * this asm block.  adr has a limited range (~±1KB encodable as ARM immediate);
 * keeping getters next to the labels guarantees the offset always fits.
 */
asm(
    ".global _saved_handlers\n"
    "_saved_handlers:\n"
    "  .word 0\n"
    "  .word 0\n"
    "  .word 0\n"
    ".global _task_active\n"
    "_task_active:\n"
    "  .word 0\n"
    ".global _spin_requested\n"
    "_spin_requested:\n"
    "  .word 0\n"
    ".global _force_win\n"
    "_force_win:\n"
    "  .word 0\n"
);

/* Tiny noinline stubs — always within a few bytes of the labels above. */
static __attribute__((noinline)) volatile uint32_t *state_handlers(void) {
    volatile uint32_t *p;
    asm volatile("adr %0, _saved_handlers" : "=r"(p));
    return p;
}
static __attribute__((noinline)) volatile uint32_t *state_active(void) {
    volatile uint32_t *p;
    asm volatile("adr %0, _task_active" : "=r"(p));
    return p;
}
static __attribute__((noinline)) volatile uint32_t *state_spin_req(void) {
    volatile uint32_t *p;
    asm volatile("adr %0, _spin_requested" : "=r"(p));
    return p;
}
static __attribute__((noinline)) volatile uint32_t *state_force_win(void) {
    volatile uint32_t *p;
    asm volatile("adr %0, _force_win" : "=r"(p));
    return p;
}

/* ── Helpers ─────────────────────────────────────────────────────────────────*/

static uint16_t *get_fb(void)
{
    uint32_t *outer = *(uint32_t **)0x0002b860u;
    if (!outer) return 0;
    uint32_t *inner = *(uint32_t **)outer;
    if (!inner) return 0;
    return (uint16_t *)(inner[0] + inner[1]);
}

static void fill_fb(uint16_t color)
{
    uint16_t *fb = get_fb();
    if (!fb) return;
    for (uint32_t i = 0; i < N_PIX; i++)
        fb[i] = color;
}

static void draw_rect(uint16_t *fb, uint16_t x, uint16_t y,
                      uint16_t w, uint16_t h, uint16_t color)
{
    for (uint16_t row = y; row < y + h && row < HEIGHT; row++)
        for (uint16_t col = x; col < x + w && col < WIDTH; col++)
            fb[row * WIDTH + col] = color;
}

static void draw_text(uint16_t *fb, const char *str,
                      uint16_t x, uint16_t y, uint16_t color)
{
    draw_string_fn draw = (draw_string_fn)AP_STATE_RESOURCE_STRING_ASCII_DRAW;
    struct ascii_str s = {
        .font_color = color,
        .font_type  = 0,
        .pos_x      = x,
        .pos_y      = y,
        .buff_w     = WIDTH,
        .buff_h     = HEIGHT,
        .str_ptr    = str,
    };
    draw(fb, &s, 2);
}

